package com.dev.rest.model;

public class Reference {

    private String code;

    private String name;

    public Reference() {
    }

    public Reference(String code, String name) {
        this.code = code;
        this.name = name;
    }

    public String getCode() {
        return code;
    }

    public String getName() {
        return name;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public void setName(String name) {
        this.name = name;
    }
}
